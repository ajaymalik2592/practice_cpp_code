const int MOD = 1e9 + 7;
const long long INF = 1e18;
const double EPS = 1e-6;
/***********************************************************************/
int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int t;
    cin >> t;
    while (t--) {
        i64 n, x, y, d;
        cin >> n >> x >> y >> d;
        i64 ans = INF;
        --x; --y;
        if (((i64) abs(y - x)) % d == 0) {
            ans = min(ans, (i64) abs(y - x) / d);
            cout << ans << '\n';
            continue;
        }
        if (y % d == 0) {
            ans = min(ans, y / d + (x + d - 1) / d);
        }
        if ((n - y - 1) % d == 0) {
            ans = min(ans, (n - y - 1) / d + (n - x - 1 + d - 1) / d);
        }
        if (ans == INF) ans = -1;
        cout << ans << '\n';
    }

    return 0;
}
