int next(int p) {
    for (int i = 0; ;i++) {
        if (p <= (1 << i)) {
            return i;
        }
    }
}
int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n;
    cin >> n;
    map<int,int> cnt;
    int p = n;
    for (int i = 2; i <= n; ++i) {
        while (p % i == 0) {
            p /= i;
            cnt[i]++;
        }
    }
    assert(p == 1);
    int ans = 1, b = 0;
    int add = 0;
    for (auto e: cnt) {
        ans *= e.first;
        p = next(e.second);
        b = max(b, p);
    }
    for (auto e: cnt) {
        if (e.second < (1 << b)) add = 1;
    }
    cout << ans << ' ' << b + add << el;

    return 0;
}
