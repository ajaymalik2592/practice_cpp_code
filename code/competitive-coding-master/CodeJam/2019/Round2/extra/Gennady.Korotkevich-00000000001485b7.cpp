constexpr long long md = 1000000123LL * 104857601;
using Mint = Modular<std::integral_constant<decay<decltype(md)>::type, md>>;

// code credit: ITMO 1 TRD (retyped by tourist)
Mint solve(long long n, long long a, long long b, long long m) {
  if (b == 0) {
    return Mint(n) * (a / m);
  }
  if (a >= m) {
    return Mint(n) * (a / m) + solve(n, a % m, b, m);
  }
  if (b >= m) {
    return Mint(n) * (n - 1) / 2 * (b / m) + solve(n, a, b % m, m);
  }
  return solve((a + b * n) / m, (a + b * n) % m, m, b);
}

int main() {
  ios::sync_with_stdio(false);
  cin.tie(0);
  int tt;
  cin >> tt;
  for (int qq = 1; qq <= tt; qq++) {
    cout << "Case #" << qq << ": ";
    int n;
    cin >> n;
    vector<int> C(n), J(n);
    for (int i = 0; i < n; i++) {
      cin >> C[i] >> J[i];
    }
    int ok = 1;
    long long low_num = 0, low_den = 1;
    long long high_num = 1, high_den = 0;
    for (int i = 0; i < n - 1; i++) {
//      C[i] * x + J[i] * y < C[i + 1] * x + J[i + 1] * y
//      y * (J[i] - J[i + 1]) < x * (C[i + 1] - C[i])
      long long cx = C[i + 1] - C[i];
      long long cy = J[i] - J[i + 1];
      if (cy >= 0 && cx <= 0) {
        ok = 0;
        break;
      }
      if (cx > 0 && cy > 0) {
        if (cx * high_den < high_num * cy) {
          high_num = cx;
          high_den = cy;
        }
      } 
      if (cx < 0 && cy < 0) {
        cx *= -1;
        cy *= -1;
        if (cx * low_den > low_num * cy) {
          low_num = cx;
          low_den = cy;
        }
      }
    }
    if (low_num * high_den >= low_den * high_num) {
      ok = 0;
    }
    if (!ok) {
      cout << "IMPOSSIBLE" << '\n';
      continue;
    }
    long long X;
    if (high_den == 0) {
      X = 1;
    } else {
      long long low = 0, high = (long long) 2e18;
      while (low < high) {
        long long mid = (low + high) >> 1;
        Mint a = solve(mid, 0, low_num, low_den);
        Mint b = solve(mid, high_den - 1, high_num, high_den) - mid + 1;
        if (a == b) {
          low = mid + 1;
        } else {
          high = mid;
        }
      }
      X = low - 1;
    }
    long long Y = (long long) ((__int128) X * low_num / low_den) + 1;
    cout << X << " " << Y << '\n';
  }
  return 0;
}
