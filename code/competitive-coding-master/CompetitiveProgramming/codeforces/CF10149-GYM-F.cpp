const int MOD = 1e9 + 7;
const long long INF = 1e18;
const double EPS = 1e-6;
/***********************************************************************/
int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int t;
    cin >> t;
    while (t--) {
        int n, k;
        cin >> n >> k;
        vector<int> a(n);
        int p = 0;
        hash_map<int,int> map;
        for (int &i: a) {
            cin >> i;
            if (map.find(i) == map.end())
                map[i] = p++;
        }
        for (int &i: a) i = map[i];
        vector<vector<int>> pos(p, vector<int>(1, 1));
        for (int i = 0; i < n; ++i) pos[a[i]].push_back(i);
        set<pair<int,int>> st;
        int ans = 0;
        set<int> cache;
        for (int i = 0; i < n; ++i) {
            int x = a[i];
            if (st.count({-i, x})) {
                st.erase({-i, x});
            } else {
                ans++;
                if ((int) st.size() == k)
                    st.erase(st.begin());
            }
            ++pos[x][0];
            if (pos[x][0] < (int) pos[x].size()) {
                st.emplace(-pos[x][pos[x][0]], x);
            } else {
                st.emplace(-MOD, x);
            }
        }
        cout << ans << '\n';
    }

    return 0;
}
